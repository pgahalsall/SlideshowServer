
var Slideshow = class {
    constructor(id, title, producer, description, slides, soundtracks, updated_date) {
        this.id = id;
        this.title = title;
        this.producer = producer;
        this.description = description;
        this.slides = slides;
        this.soundtracks = soundtracks;
        this.updated_date = updated_date;
    }
}